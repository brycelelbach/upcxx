package Events;


########################################################################


sub new ($) {
    my $proto = shift;
    bless [], $proto;
}


########################################################################


sub push ($$) {
    my ($self, $event) = @_;
    push @$self, $event;
}


########################################################################


sub print ($$) {
    my ($self, $out) = @_;
    @$self = sort { $a->{'time'} <=> $b->{'time'} } @$self;
    
    $out->print('/numEvents ' . @$self . " def\n");

    foreach (@$self) {
	$_->print($out);
    }
}


########################################################################


1;
