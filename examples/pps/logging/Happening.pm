package Happening;


########################################################################


sub new ($$$) {
    my ($proto, $proc) = @_;
    my $class = ref($proto) || $proto;

    my $self = { 'proc' => $proc };    
    bless $self, $class;
}


########################################################################


sub proc ($) {
    return shift->{proc};
}


########################################################################


sub print ($$) {
    my ($self, $out) = @_;
    my @params = $self->params;
    $out->print("@params\n");
}


########################################################################


1;
